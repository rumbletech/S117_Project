void s117_write_num ( uint32_t num ) {


	uint8_t temp ;
	uint8_t vals[4];
	/* Enable all Digits */
	 s117_write( 0xff , 2 );

	 for ( int i = 0 , k = 0  ; i < 8 ; i++ , num/= 10 )
	 {
		 if ( k %2 == 0 ) {
		 temp = num % 10 ;
		 }
		 else {
			  temp = temp | ( (num%10) <<  4 );
			  vals[k/2] = temp ;
		 }

		 k++ ;

	 }

	 for ( int i = 0 ; i < 4 ; i++ ) {
	 s117_write( vals[i] , 0x4 + i ) ;


	 }


}


